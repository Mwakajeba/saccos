<div class="card border-info mb-3">
    <div class="card-header bg-info text-white">
        <h6 class="mb-0"><i class="bx bx-info-circle me-2"></i>Loan Application Guidelines</h6>
    </div>
    <div class="card-body">
        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-money me-1"></i>What is a Loan?</h6>
            <p class="small mb-0">A loan is a financial agreement where the SACCOS lends money to a member, who agrees to repay it with interest over a specified period.</p>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-list-ul me-1"></i>Required Information</h6>
            <ul class="small mb-0">
                <li><strong>Customer:</strong> The member borrowing the loan</li>
                <li><strong>Loan Product:</strong> Type of loan (determines terms, interest, period)</li>
                <li><strong>Amount:</strong> Principal amount to be borrowed</li>
                <li><strong>Period:</strong> Repayment duration in months</li>
                <li><strong>Interest Rate:</strong> Annual percentage rate (from product or custom)</li>
                <li><strong>Application Date:</strong> When the loan was applied for</li>
                <li><strong>Loan Officer:</strong> Staff member handling the loan</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-shield-check me-1"></i>Guarantors & Collateral</h6>
            <ul class="small mb-0">
                <li><strong>Guarantors:</strong> Members who pledge to repay if borrower defaults</li>
                <li><strong>Collateral:</strong> Physical assets pledged as security</li>
                <li><strong>Cash Collateral:</strong> Savings or deposits held as security</li>
                <li>Requirements depend on loan product settings</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-refresh me-1"></i>Loan Lifecycle</h6>
            <ul class="small mb-0">
                <li><strong>Applied:</strong> Initial application submitted</li>
                <li><strong>Checked:</strong> Under review by loan officer</li>
                <li><strong>Approved:</strong> Board/committee approved</li>
                <li><strong>Authorized:</strong> Final authorization granted</li>
                <li><strong>Disbursed:</strong> Funds released to member</li>
                <li><strong>Completed:</strong> Fully repaid</li>
                <li><strong>Defaulted:</strong> Payments significantly overdue</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-calculator me-1"></i>Interest Calculation</h6>
            <ul class="small mb-0">
                <li><strong>Flat Rate:</strong> Interest on original amount throughout</li>
                <li><strong>Reducing Balance:</strong> Interest on remaining balance (recommended)</li>
                <li><strong>Interest Cycle:</strong> How often interest is calculated (monthly, weekly, etc.)</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-target-lock me-1"></i>Business Sector</h6>
            <p class="small mb-0">Select the economic sector for the loan purpose. This helps with portfolio analysis, risk assessment, and regulatory reporting.</p>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-check-circle me-1"></i>Before Disbursement</h6>
            <ul class="small mb-0">
                <li>Verify customer eligibility</li>
                <li>Check guarantor availability and capacity</li>
                <li>Verify collateral value (if required)</li>
                <li>Ensure proper approvals are obtained</li>
                <li>Confirm disbursement bank account</li>
                <li>Review repayment schedule with borrower</li>
            </ul>
        </div>

        <div class="alert alert-warning mb-0">
            <i class="bx bx-error me-1"></i>
            <strong>Important:</strong> Ensure all loan applications comply with SACCOS policies, regulatory requirements, and responsible lending practices.
        </div>
    </div>
</div>
