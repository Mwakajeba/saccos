<div class="card border-primary mb-3">
    <div class="card-header bg-primary text-white">
        <h6 class="mb-0"><i class="bx bx-info-circle me-2"></i>Loan Product Guidelines</h6>
    </div>
    <div class="card-body">
        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-credit-card me-1"></i>What is a Loan Product?</h6>
            <p class="small mb-0">A loan product defines the terms, conditions, interest rates, and repayment rules for a specific type of loan offered by your SACCOS.</p>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-list-ul me-1"></i>Key Components</h6>
            <ul class="small mb-0">
                <li><strong>Product Name:</strong> Clear identifier (e.g., "Emergency Loan", "Business Development Loan")</li>
                <li><strong>Interest Rate:</strong> Annual percentage rate charged on the loan</li>
                <li><strong>Loan Limits:</strong> Minimum and maximum amounts that can be borrowed</li>
                <li><strong>Repayment Period:</strong> Duration for loan repayment (in months)</li>
                <li><strong>Fees:</strong> Processing fees, insurance, or other charges</li>
                <li><strong>Penalty Rates:</strong> Late payment penalties</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-calculator me-1"></i>Interest Calculation Methods</h6>
            <ul class="small mb-0">
                <li><strong>Flat Rate:</strong> Interest calculated on original principal throughout loan term</li>
                <li><strong>Reducing Balance:</strong> Interest calculated on outstanding balance (common)</li>
                <li><strong>Compound Interest:</strong> Interest on both principal and accumulated interest</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-calendar me-1"></i>Repayment Frequencies</h6>
            <ul class="small mb-0">
                <li><strong>Daily:</strong> Payments due every day</li>
                <li><strong>Weekly:</strong> Payments due every 7 days</li>
                <li><strong>Bi-Weekly:</strong> Payments every 14 days</li>
                <li><strong>Monthly:</strong> Most common - payments due monthly</li>
                <li><strong>Quarterly:</strong> Payments every 3 months</li>
                <li><strong>Annual:</strong> Yearly payments</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-check-shield me-1"></i>Collateral & Guarantors</h6>
            <ul class="small mb-0">
                <li><strong>Collateral Required:</strong> Physical assets pledged as security</li>
                <li><strong>Guarantors:</strong> Number of guarantors required to secure the loan</li>
                <li><strong>Collateral Value:</strong> Minimum percentage of loan amount</li>
            </ul>
        </div>

        <div class="mb-3">
            <h6 class="text-primary"><i class="bx bx-target-lock me-1"></i>Best Practices</h6>
            <ul class="small mb-0">
                <li>Set realistic interest rates based on market and costs</li>
                <li>Define clear eligibility criteria</li>
                <li>Ensure product aligns with SACCOS mission</li>
                <li>Consider members' repayment capacity</li>
                <li>Review and update products regularly</li>
            </ul>
        </div>

        <div class="alert alert-warning mb-0">
            <i class="bx bx-info-circle me-1"></i>
            <strong>Important:</strong> All loan products must comply with regulatory requirements and be approved by the SACCOS board before activation.
        </div>
    </div>
</div>
